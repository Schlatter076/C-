//-----------------------------------------------------------------------------
// Number to String Routine
//-----------------------------------------------------------------------------
char * Number_String ( char * p, char c )
{
	if ( c < 10 )
	{
		*p = '0';
		*(p+1) = '0' | c;
	}
	else if ( c < 100 )
	{
		*p = '1';
		*(p+1) = '0' | ( c / 10 );
	}
	else
		p = 0;
	return p;
}
